# Function for list

list = [1, 2, 3, 4, 5]

# len
print("len, count itens of list")
print(len(list))
print("")

# sum
print("sum, sum all itens of list")
print(sum(list))
print("")

# max
print("max, max value of list")
print(max(list))
print("")

# min
print("min, min value of list")
print(min(list))
